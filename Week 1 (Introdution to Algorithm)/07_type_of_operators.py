#In python, the different type of operators: arithmetic, relational and logical.

#Arthemetic (+, -, *, /, //, %, **)

print("Addition", 7+8)
print("Subraction", 3-2)
print("Multiplication", 9*3)
print("Division", 7/3)
print("floor division", 7//3)
print("Modulus", 7%3) 
print("Exponentiation", 7**3)
print("---------------------")

#Relational (<, >, <=, >=, ==, !=)

print(4<9)
print(4>9)
print(5<=5)
print(5>=50)
print("ads"=="ads")
print("ads"!="jam")
print("--------------------")

#Logical (and, or, not)

print(True and True)
print(True and False)
print(False and False)
print(False and True)

print(True or True)
print(True or False)
print(False or False)
print(False or True)

print(not(True))
print(not False)