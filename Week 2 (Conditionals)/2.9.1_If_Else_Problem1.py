# To check whether the input no. is even or odd

n = int(input('Enter the number: '))
if(n%2 == 0):  # (When we use a single equals to sign "=" then it means you are assigning a value to something, means you are assigning a some value to a variable but when you want to compare 2 values or variables then we use double equals to sign "==")
  print('even')
else:
  print('odd')