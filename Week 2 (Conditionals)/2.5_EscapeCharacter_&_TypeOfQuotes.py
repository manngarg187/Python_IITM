# Escape Character (backslash "\")
# Error: print('It's a beautiful day.')
print('It\'s a beautiful day.')
print("I am a student of \"IIT\" Madras.")
# "Tab" Escape character ' \t (t stands for tab) '
print('My name is Mann Garg. I am a very curious person.')
print('My name is Mann Garg. \tI am a very curious person.')
print('My name is Mann Garg. \t\tI am a very curious person.')
# "New line" Escape character " \n (n stands for new line) "
print('My name is Mann Garg. I am a very curious person.')
print('My name is Mann Garg. \nI am a very curious person.')
print('My name is Mann Garg.\n\nI am a very curious person.')

print("It's a beautiful day.")
print('I am a student of "IIT" Madras.')

